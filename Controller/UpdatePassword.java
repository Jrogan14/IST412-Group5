package Controller;

/**
 * This class allows users to update an existing password.
 */
public class UpdatePassword {

    /**
     * This method takes user's old password as a parameter and changes it to new given password.
     * @param password - Old password passes into method.
     * @return new password of user's choice.
     */
    public String getNewPasswords(String password){
        String newPassword = "";
        password += newPassword;
        
        return newPassword;
    }
}
